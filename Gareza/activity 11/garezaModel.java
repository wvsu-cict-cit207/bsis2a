package finals;

public class garezaModel {
    public static int add(int num1, int num2){
        return num1 + num2;
    }
    public static int min(int num1, int num2){
        return num1 - num2;
    }
    public static int mul(int num1, int num2){
        return num1 * num2;
    }
    public static double div(double num1, double num2){
        return num1 / num2;
    }
    public static double quo(double num1,double num2){
        return num1 % num2;
    }
}
