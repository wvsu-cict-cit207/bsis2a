package com.jetbrains;

public class GUImodel {
    public static int addNumbers(int num1, int num2){
        return num1 + num2;
    }
    public static int diffNumbers(int num1, int num2){
        return num1 - num2;

    }
    public static int timesNumbers(int num1, int num2){
        return num1 * num2;

    }
    public static int divNumbers(int num1, int num2){
        return num1 / num2;

    }
    public static int moduloNumbers(int num1, int num2) {
        return num1 % num2;

    }

}
